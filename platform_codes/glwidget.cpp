void GLWidget::setAllUnifromCompute()
{
    if(m_wrapper == nullptr){
        m_programComputeVertices->setUniformValue("t", clock()/1000.0f);
    }else{
        m_wrapper->refresh();
        for(InfoWrapper::const_iteratorType beg = m_wrapper->cbegin(), end = m_wrapper->cend(); beg != end; ++beg){
            m_programComputeVertices->setUniformValue((const char *)(beg->first.c_str()), float(beg->second));
        }
    }
}

void GLWidget::initializeGL() {
    connect(context(), &QOpenGLContext::aboutToBeDestroyed, this, &GLWidget::cleanup);
    initializeOpenGLFunctions();
    glClearColor(0.0, 0.0, 0.0, 0);

    //create buffers
    glGenBuffers(1, &m_verticesData);
    glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 0, m_verticesData);
    glNamedBufferData(m_verticesData,  32 * 32 * 32 * sizeof(GLfloat)* 4 * 16, 0, GL_DYNAMIC_DRAW);

    glGenBuffers(1, &m_trigTable);
    glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 1, m_trigTable);
    glNamedBufferData(m_trigTable, sizeof(triTable), triTable, GL_STATIC_DRAW);

    glGenBuffers(1, &m_temp);
    glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 2, m_temp);
    glNamedBufferData(m_temp, sizeof(GLfloat) * 4 * 4 * 4 * 3000 * 4, 0, GL_DYNAMIC_DRAW);


    glGenBuffers(1, &m_debug);
    glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 8, m_debug);
    glNamedBufferData(m_debug, sizeof(GLfloat)* 80 * m_centerNum, 0, GL_DYNAMIC_DRAW);
    glClearNamedBufferSubData(m_debug, GL_R8, 0, sizeof(GLfloat)* 64 * m_centerNum, GL_RGBA, GL_UNSIGNED_BYTE, 0);

    //create these programs
    m_programComputeVertices->addShaderFromSourceFile(QOpenGLShader::Compute, "C:/Users/yyc19/Documents/Qt/OpenGlFunc/marchingCubes.compute");
    m_programComputeRearrange.addShaderFromSourceFile(QOpenGLShader::Compute, "C:/Users/yyc19/Documents/Qt/OpenGlFunc/marchingCubesRearrange.compute");
    m_programRender.addShaderFromSourceFile(QOpenGLShader::Vertex, "C:/Users/yyc19/Documents/Qt/OpenGlFunc/vsshader.vert");
    m_programRender.addShaderFromSourceFile(QOpenGLShader::Fragment, "C:/Users/yyc19/Documents/Qt/OpenGlFunc/fgshader.frag");

    m_programComputeVertices->link();
    m_programComputeRearrange.link();
    m_programComputeVertices->bind();
    m_programComputeVertices->setUniformValue("isolevel", 0.0f);
    m_programComputeVertices->setUniformValue("width", 4.0f/32.0f);

    m_programRender.link();
    m_programRender.bind();
    m_camera.setToIdentity();
    m_camera.lookAt(QVector3D(0.0, 0.0, m_h),QVector3D(0.0, 0.0, 0.0),QVector3D(0.0, 1.0, 0.0));
    m_worldLoc = m_programRender.uniformLocation("world");
    m_projLoc = m_programRender.uniformLocation("proj");
    m_cameraLoc = m_programRender.uniformLocation("camera");

    //gen VAO
    glGenVertexArrays(1, &m_VAO);
    glBindVertexArray(m_VAO);
    glVertexArrayVertexBuffer(m_VAO, 0, m_verticesData , 4 * sizeof(GLfloat), 4 * sizeof(GLfloat));
    glVertexArrayAttribFormat(m_VAO, 0, 4 * sizeof(GLfloat),GL_FLOAT, GL_FALSE, 0);
    glVertexArrayAttribBinding(m_VAO, 0, 0);
    glEnableVertexArrayAttrib(m_VAO, 0);
    glPointSize(3);
}


void GLWidget::paintGL(){
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
    glEnable(GL_DEPTH_TEST);

    //compute center
    glClearNamedBufferSubData(m_verticesData, GL_R8, 0, sizeof(GLuint), GL_RGBA, GL_UNSIGNED_BYTE, 0);
    if(m_renewflag){
        delete m_programComputeVertices;
        m_programComputeVertices = new QOpenGLShaderProgram;
        m_programComputeVertices->addShaderFromSourceCode(QOpenGLShader::Compute, m_wrapper->shaderCode());
        m_programComputeVertices->link();
        m_programComputeVertices->bind();
        m_programComputeVertices->setUniformValue("isolevel", 0.0f);
        m_programComputeVertices->setUniformValue("width", m_wrapper->width());
        m_xrange = m_wrapper->xwidth();
        m_yrange = m_wrapper->ywidth();
        m_zrange = m_wrapper->zwidth();
        unsigned int Mrange = std::max(m_xrange, std::max(m_yrange, m_zrange)) * m_wrapper->width() * 8;
        m_h = Mrange / 4.0f  * 20.0f;
        m_camera.setToIdentity();
        m_camera.lookAt(QVector3D(0.0, 0.0, m_h),QVector3D(0.0, 0.0, 0.0),QVector3D(0.0, 1.0, 0.0));
        m_renewflag = false;
    }
    m_programComputeVertices->bind();
    setAllUnifromCompute();
    glDispatchCompute(m_xrange, m_yrange, m_zrange);
    glMemoryBarrier(GL_SHADER_STORAGE_BARRIER_BIT);
    m_programComputeRearrange.bind();
    glDispatchCompute(1, 1, 1);
    glMemoryBarrier(GL_VERTEX_ATTRIB_ARRAY_BARRIER_BIT );
    m_verticesNum = *(unsigned int *)glMapNamedBufferRange(m_verticesData, 0, sizeof(GLuint), GL_MAP_READ_BIT);
    glUnmapNamedBuffer(m_verticesData);
    m_programRender.bind();
    m_world.setToIdentity();

    m_world.translate( m_objR, 0.0, 0.0);
    m_world.rotate(0, 1, 0, 0);
    m_world.rotate(90, 0, 1, 0);
    m_world.rotate(clock()/300.0f, 0, 0, 1);

    m_programRender.setUniformValue(m_worldLoc, m_world);
    m_programRender.setUniformValue(m_projLoc, m_proj);
    m_programRender.setUniformValue(m_cameraLoc, m_camera);
    glDrawArrays(GL_TRIANGLES, 0, m_verticesNum);

    m_world.setToIdentity();

    m_world.translate( 0.0, -m_objR, 0.0);
    m_world.rotate(90, 1, 0, 0);
    m_world.rotate(0, 0, 1, 0);
    m_world.rotate(clock()/300.0f, 0, 0, 1);



    m_programRender.setUniformValue(m_worldLoc, m_world);

    glDrawArrays(GL_TRIANGLES, 0, m_verticesNum);

    m_world.setToIdentity();

    m_world.translate( -m_objR, 0.0, 0.0);
    m_world.rotate(0, 1, 0, 0);
    m_world.rotate(-90, 0, 1, 0);
    m_world.rotate(clock()/300.0f, 0, 0, 1);


    m_programRender.setUniformValue(m_worldLoc, m_world);

    glDrawArrays(GL_TRIANGLES, 0, m_verticesNum);
    m_world.setToIdentity();

    m_world.translate( 0.0,m_objR,  0.0);
    m_world.rotate(- 90, 1, 0, 0);
    m_world.rotate(0, 0, 1, 0);
    m_world.rotate(clock()/70.0f, 0, 0, 1);

    m_programRender.setUniformValue(m_worldLoc, m_world);

    glDrawArrays(GL_TRIANGLES, 0, m_verticesNum);

    //update
    emit update();
}

void GLWidget::resizeGL(int w, int h){
    float ra = float(w)/ h;
    float sqrt2 = 1.414;
    m_proj.setToIdentity();
    m_proj.perspective(45.0f, GLfloat(w) / h, 0.01f, 100.0f);
    m_objR = sqrt(1 / (sqrt2 /4 * ra * ra - float(1)/ 4 * ra * ra + sqrt2/4 + float(1)/2)) * m_h / 4;
}
